    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int linha = tabela.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else if(JOptionPane.showConfirmDialog(null, "Deseja realmente deletar?"
                ,"Confirmar Exclus√£o", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            
            pc = repo.pesquisarPorId((int) tabela.getValueAt(linha, 0));
            repo.deletar(pc);
            atualizarTabela();
            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int linha = tabela.getSelectedRow();
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }else{
            pc = repo.pesquisarPorId((int) tabela.getValueAt(linha, 0));
            CadastrarPodCast tela = new CadastrarPodCast(adm);
            tela.preencherCampos(pc);
            tela.setVisible(true);
            dispose();
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

